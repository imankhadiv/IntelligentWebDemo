package beans;

public class Tweet {
	
	private String shortUrl;
	private String content;
	private String tweetId;
	private String hasOriginalTweet;
	private String tweet;
	private String date;
	private String retweetPeople;
	private String hasVenue;
	public String getShortUrl() {
		return shortUrl;
	}
	public void setShortUrl(String shortUrl) {
		this.shortUrl = shortUrl;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTweetId() {
		return tweetId;
	}
	public void setTweetId(String tweetId) {
		this.tweetId = tweetId;
	}
	public String getHasOriginalTweet() {
		return hasOriginalTweet;
	}
	public void setHasOriginalTweet(String hasOriginalTweet) {
		this.hasOriginalTweet = hasOriginalTweet;
	}
	public String getTweet() {
		return tweet;
	}
	public void setTweet(String tweet) {
		this.tweet = tweet;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getRetweetPeople() {
		return retweetPeople;
	}
	public void setRetweetPeople(String retweetPeople) {
		this.retweetPeople = retweetPeople;
	}
	public String getHasVenue() {
		return hasVenue;
	}
	public void setHasVenue(String hasVenue) {
		this.hasVenue = hasVenue;
	}
	
	
	
	
	

}
